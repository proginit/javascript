// Definiciones de Variables var, let, const

// solo se puede modificar si esta despues de definir el valor de la variable
let nombre = "Alex";
console.log(nombre);

// forma antigua/ es mas de forma global para reutilizar
var nombre2 = "Jose";
console.log(nombre2);

// Variable const // no se puede modificar el valor de la variable
const pais = "Bolivia";
console.log(pais);
